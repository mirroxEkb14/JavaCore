package AbstractClass.BasisConcepts;

public class AbstractClassesVsInterfaces {

    /**
     * Use Abstract classes if:
     *      the code is shared between closely connected classes;
     *      descendant classes have lots of common methods or fields
     *          or require non-public fields(protected, private);
     *      non-final fields are declared
     *
     * Use interfaces if:
     *      unrelated classes implement interface;
     *      use multiple type inheritance
     */
}
